DROP DATABASE javatraining;
CREATE DATABASE javatraining CHARACTER SET UTF8;
USE javatraining;

CREATE TABLE BOOK_INFO(
    PROD_ID INT(10) AUTO_INCREMENT NOT NULL, 
    BOOK_NAME VARCHAR(30) NOT NULL,
    AUTHOR VARCHAR(30) NOT NULL,
    AMOUNT INT(10) NOT NULL,
    LOCATION VARCHAR(30) NOT NULL,
    DESCRIPTION VARCHAR(100),
    PRIMARY KEY (PROD_ID)
    );

CREATE TABLE USERS(
    USER_ID INT(10) AUTO_INCREMENT NOT NULL, 
    USER_NAME VARCHAR(30) NOT NULL,
    USER_PASSWORD VARCHAR(30) NOT NULL,
    PRIMARY KEY (USER_ID)
    );
    
CREATE TABLE COMMUNITIES(
	COMMUNITY_ID INT(10) AUTO_INCREMENT NOT NULL, 
    COMMUNITY_NAME VARCHAR(30) NOT NULL,
    COMMUNITY_PASSWORD VARCHAR(30) NOT NULL,
    PRIMARY KEY (COMMUNITY_ID)
    );

